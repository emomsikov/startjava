public class Variable {
	public static void main(String[] args) {
		byte processorCore = 4;
		System.out.println(processorCore);

		short systemType = 64;
		System.out.println(systemType);

		int windowsUpdVerison = 2004;
		System.out.println(windowsUpdVerison);

		long irqChannel = 4294967267L;
		System.out.println(irqChannel);

		float ram = 8.00F;
		System.out.println(ram);

		double hdd = 930.27;
		System.out.println(hdd);

		char favoriteCharacter = 'q';
		System.out.println(favoriteCharacter);

		boolean cdRom = false;
		System.out.println(cdRom);
	}
}